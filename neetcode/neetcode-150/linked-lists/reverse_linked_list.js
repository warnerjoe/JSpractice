class Solution {
    /**
     * @param {ListNode} head
     * @return {ListNode}
     */
    reverseList(head) {
        let prev = null
        let curr = head

        while (curr) {
            let temp = curr.next // temp = a.next (B)
            curr.next = prev
            prev = curr
            curr = temp
        }
        return prev
    }
}

